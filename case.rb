current_weather = "rainy"

case current_weather
when "sunny"
	puts "You're super pale- Put on some sunscreen!"
when "foggy"
	puts "Not good driving weather!"
when "rainy"
	puts "Yay rainy days! Maybe stay inside and read..."
when "snowy"
	puts "Stay warm!"
when "cloudy"
	puts "Bring a light jacket!"
else
	puts "Hmm... Not sure what it's doing outside!"
end